export const getTestimonialsByRating = (rating) => {
  return testimonials.filter(t => t.rating === rating);
};

export const getTestimonialsByPlan = (planId) => {
  return testimonials.filter(t => t.plan.toLowerCase() === planId.toLowerCase());
};

export const getFeaturedTestimonials = (count = 3) => {
  return testimonials
    .sort((a, b) => b.helpfulCount - a.helpfulCount)
    .slice(0, count);
};

export const getVerifiedTestimonials = () => {
  return testimonials.filter(t => t.verified);
};

